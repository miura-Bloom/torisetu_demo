// 各HTMLファイルを読み込んで持ち味部分を置換
Object.entries(strengths).forEach(([id, items]) => {
  const filePath = path.join(__dirname, `${id}.html`);
  if (!fs.existsSync(filePath)) {
    console.log(`スキップ: ${id}.html が見つかりません`);
    return;
  }
  
  let html = fs.readFileSync(filePath, 'utf8');
  
  // 新しいstrengths HTML
  const newStrengthsHTML = items.map(s => `<li>${s}</li>`).join('');
  
  // ul.strengthsの中身を置換
  html = html.replace(/<ul class="strengths">[\s\S]*?<\/ul>/, `<ul class="strengths">${newStrengthsHTML}</ul>`);
  
  fs.writeFileSync(filePath, html, 'utf8');
  console.log(`✓ ${id}`);
});

